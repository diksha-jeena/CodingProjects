tpl = eval(input("Enter your marks of 5 subjects :"))
total = sum(tpl)
avg = total/5
if(avg >= 75):
    print("Total marks =",total , "Grade = A")
elif(avg <= 74.999 and avg >= 60):
    print("Total marks =",total , "Grade = B")
elif(avg <= 59.999 and avg >= 50):
    print("Total marks =",total , "Grade = C")
elif(avg < 50):
    print("Total marks =",total , "Grade = D")


