# Original list of characters
name = ["d", "i", "k", "s", "h", "a"]

# Method 1: Concatenate characters to form a string
name2 = ""
for i in name:
    name2 = name2 + i
print(name2)

# Method 2: Create a new list with individual characters
name3 = []
for i in name:
    name3.append(i)
print(name3)
